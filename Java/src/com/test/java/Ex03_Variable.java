package com.test.java;

public class Ex03_Variable {

	public static void main(String[] args) {
		
		/*
		 * 변수, Variable 
		 * 정의: 개발자가 명령어를 사용해서 할당받은 메모리 공간 
		 * 목적: 원하는 데이터를 읽거나 쓰기 위한 공간
		 */
		
//		1. 생성하기 : 자료형 변수명;
		byte kor;
//		2. 초기화하기 : 변수 = 값;
		kor = 100;		
//		3. 사용하기
		System.out.println(kor); //이거 뭐지 > 국어점수 > 표현으로 의미를 알 수 있음
		System.out.println(100); //이거 뭐지 > ?? > 포현으로 의미를 알 수 없음
	}
}

